function search(roll){
	var res = 
	{
		success: false,
		unit: '',
		centre_name: '',
		centre_loc: '',
		centre_dir: '',
		centre_lat: 22.791833,
		centre_lng: 91.103065,
		centre_img: ''
	}
	
	var range_count = seat_plan.ranges.length;
	for(var i=0; i<range_count; i++)
	{		
		var r = seat_plan.ranges[i];
		{
			if(r.range_start <= roll && r.range_end >= roll)
			{
				res.centre_id = r.centre_id;
				res.unit = r.unit;
			}
		}
	}
	//alert(centre_id);
	if(res.centre_id > 0)
	{
		res.success = true;
		var centre_count = seat_plan.centres.length;
		for(var i=0; i<centre_count; i++)
		{
			var c = seat_plan.centres[i];
			if(c.centre_id == res.centre_id)
			{
				res.centre_name = c.centre_name;
				res.centre_dir = c.direction;
				res.centre_loc = c.location;
				res.centre_lat = c.lat;
				res.centre_lng = c.lng;
				res.centre_img = c.photo;
			}
		}
	}
	return res;
}

function show_all_seat_plans()
{
	var table_html="<table><tbody>";


	var centres=[];
	var centre_count = seat_plan.centres.length;
	for(var i=0; i<centre_count; i++)
	{
		var c = seat_plan.centres[i];
		centres[c.centre_id] = c.centre_name;
	}

	var range_count = seat_plan.ranges.length;
	for(var i=0; i<range_count; i++)
	{		
		var r = seat_plan.ranges[i];

		table_html += "<tr>";
		table_html += "<td>"+r.unit+"</td>";
		table_html += "<td>"+centres[r.centre_id]+"</td>";
		table_html += "<td>"+r.range_start+"</td>";
		table_html += "<td>"+r.range_end+"</td>";
		table_html += "</tr>";
		
	}

	table_html +="</tbody></table>";

	return table_html;
	
}

